local baseCharacters = {}

for name, info in pairs(SoccerCharacters) do
	if not string.find(name, "GOLD ") then
		baseCharacters[name] = info
	end
end

for _, Info in pairs(baseCharacters) do
	if not string.find(Info.Name, "GOLD ") then
		if Info.Rarity == "Legendary" then
			local GoldName = `GOLD {Info.Name}`

			SoccerCharacters[GoldName] = {
				["Name"] = Info.Name,
				["Multipliers"] = {
					["Shoot"] = Info.Multipliers.Shoot * 2,
					["Dribble"] = Info.Multipliers.Dribble * 2,
					["Pass"] = Info.Multipliers.Pass * 2,
				},
				["Rarity"] = "Gold Legendary",
				["Nationality"] = Info.Nationality,
			}
		end
	end
end

return SoccerCharacters
